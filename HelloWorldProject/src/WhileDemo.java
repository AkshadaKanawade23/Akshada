
public class WhileDemo {

	public static void main(String[] args) {
 
		int i=2;
		while(i<=5)
		{
			int j=1;
			while(j<=10)
			{
				System.out.println(i+ "*" +j+"="+i*+j);
				j++;
		    }
		 System.out.println();
		 i++;
	    }
		
		System.out.println("End of the Loop");
    }
}